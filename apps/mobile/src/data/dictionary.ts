/** Deterministic "Word of the Day" based on the date. */
export function wordOfTheDay(date = new Date()): WordEntry {
  const dayIndex =
    date.getFullYear() * 1000 + // year offset
    (date.getMonth() + 1) * 31 +
    date.getDate();
  return DICTIONARY[dayIndex % DICTIONARY.length];
}
